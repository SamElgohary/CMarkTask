package elgohary.r.sam.cmarktask.Model;

public class User {

    String id;
    String userName;
    String userEmail;
    String userAddress;
    String userImg;
    String userPhoneNumper;
    String userPassword;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getUserAddress() {
        return userAddress;
    }

    public void setUserAddress(String userAddress) {
        this.userAddress = userAddress;
    }

    public String getUserImg() {
        return userImg;
    }

    public void setUserImg(String userImg) {
        this.userImg = userImg;
    }

    public String getUserPhoneNumper() {
        return userPhoneNumper;
    }

    public void setUserPhoneNumper(String userPhoneNumper) {
        this.userPhoneNumper = userPhoneNumper;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }
}


